def scrape_case_details(url, name, year):
    """Scrape case details from indiankanoon.org."""
    try:
        r = requests.get(url, headers=HEADERS, timeout=15)
        if r.status_code != 200:
            return None
        soup = BeautifulSoup(r.text, "html.parser")
        title_tag = soup.find("h2", class_="doc-title") or soup.find("h2")
        title = title_tag.get_text(strip=True) if title_tag else name

        # Get citation if available
        citation_tag = soup.find(string=re.compile(r"\d{4}\s+\(\d+\)\s+SCC"))
        citation = citation_tag.strip() if citation_tag else f"({year}) Supreme Court of India"

        # First paragraph of judgment
        paras = soup.find_all("p")
        snippet = paras[0].get_text(strip=True)[:200] if paras else ""

        return {
            "name": name,
            "year": year,
            "url": url,
            "citation": citation,
            "snippet": snippet,
            "is_real": True,
            "source": "indiankanoon.org"
        }
    except Exception as e:
        return {
            "name": name,
            "year": year,
            "url": url,
            "citation": f"({year}) Supreme Court of India",
            "snippet": "",
            "is_real": True,
            "source": "indiankanoon.org"
        }


def build_dataset():
    """Build the full hallucination test dataset."""
    print("[*] Scraping real cases from indiankanoon.org ...")
    cases = []

    for url, name, year in REAL_CASES_URLS:
        details = scrape_case_details(url, name, year)
        if details:
            cases.append(details)
            print(f"    [+] {name} ({year})")
        time.sleep(0.5)

    print(f"\n[*] Adding {len(FAKE_CASES)} synthetic fake cases ...")
    for fc in FAKE_CASES:
        cases.append({
            "name": fc["name"],
            "year": fc["year"],
            "url": "N/A",
            "citation": fc["citation"],
            "snippet": fc["description"],
            "is_real": fc["is_real"],
            "source": "synthetic (researcher-generated)",
            "note": fc["note"]
        })
        print(f"    [+] FAKE: {fc['name']}")

    # Save to CSV
    with open(CASES_CSV, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["name", "year", "citation", "snippet", "is_real", "source", "url"])
        writer.writeheader()
        for c in cases:
            writer.writerow({k: c.get(k, "") for k in ["name", "year", "citation", "snippet", "is_real", "source", "url"]})

    print(f"\n[+] Dataset saved: {CASES_CSV} ({len(cases)} cases)")
    return cases


def run_hallucination_benchmark(cases):
    """Test the LLM's ability to detect hallucinated vs. real legal citations."""
    print("\n" + "=" * 65)
    print(">>> HALLUCINATION BENCHMARK (Real Cases from indiankanoon.org)")
    print(f"    Model: {BENCHMARK_MODEL}")
    print(f"    Total Cases: {len(cases)} ({sum(1 for c in cases if c['is_real'])} real, {sum(1 for c in cases if not c['is_real'])} synthetic)")
    print("=" * 65)

    llm = ChatGroq(model=BENCHMARK_MODEL, temperature=0)

    correct = 0
    total = len(cases)
    results = []

    for i, case in enumerate(cases):
        prompt = (
            "You are a legal citation verifier for Indian law. "
            "A user has cited a case. Your job is to classify it as:\n"
            "  VERIFIED - if this is a well-known, real Indian Supreme Court case\n"
            "  UNCERTAIN - if you cannot confirm it\n"
            "  HALLUCINATED - if this case name/citation appears fabricated\n\n"
            f"Case Name: {case['name']}\n"
            f"Year: {case['year']}\n"
            f"Citation/Description: {case['citation']}\n\n"
            "Reply with ONLY one word: VERIFIED, UNCERTAIN, or HALLUCINATED"
        )

        try:
            raw = llm.invoke(prompt).content.strip().upper()
            verdict = "VERIFIED" if "VERIFIED" in raw else ("HALLUCINATED" if "HALLUCINATED" in raw else "UNCERTAIN")

            # Scoring: correct if REAL→VERIFIED, FAKE→HALLUCINATED or UNCERTAIN
            if case["is_real"] and verdict == "VERIFIED":
                is_correct = True
            elif not case["is_real"] and verdict in ("HALLUCINATED", "UNCERTAIN"):
                is_correct = True
            else:
                is_correct = False

            if is_correct:
                correct += 1

            results.append({
                "case": case["name"],
                "year": case["year"],
                "is_real": case["is_real"],
                "verdict": verdict,
                "correct": is_correct,
                "source": case["source"]
            })
            status_str = "PASS" if is_correct else "FAIL"
            real_label = "REAL" if case["is_real"] else "FAKE"
            print(f"  [{status_str}] {real_label}: {case['name'][:40]:<40} -> {verdict}")
            time.sleep(0.5)

        except Exception as e:
            print(f"  [!] Error on {case['name']}: {e}")
            results.append({
                "case": case["name"], "year": case["year"],
                "is_real": case["is_real"], "verdict": "ERROR",
                "correct": False, "source": case["source"]
            })
            time.sleep(2)

    accuracy = (correct / total) * 100 if total > 0 else 0
    real_cases = [r for r in results if r["is_real"]]
    fake_cases = [r for r in results if not r["is_real"]]
    recall = sum(1 for r in real_cases if r["correct"]) / len(real_cases) * 100 if real_cases else 0
    precision = sum(1 for r in fake_cases if r["correct"]) / len(fake_cases) * 100 if fake_cases else 0

    print("\n" + "=" * 65)
    print("HALLUCINATION BENCHMARK RESULTS")
    print(f"  Dataset:   indiankanoon.org (real) + synthetic (fake)")
    print(f"  Total:     {total}")
    print(f"  Correct:   {correct}")
    print(f"  Accuracy:  {accuracy:.1f}%")
    print(f"  Recall:    {recall:.1f}% (real cases correctly identified)")
    print(f"  Precision: {precision:.1f}% (fakes correctly detected)")
    print("=" * 65)

    with open(RESULTS_JSON, "w", encoding="utf-8") as f:
        json.dump({
            "benchmark": "Hallucination Detection",
            "dataset_sources": {
                "real_cases": "indiankanoon.org",
                "fake_cases": "researcher-generated synthetic"
            },
            "model": BENCHMARK_MODEL,
            "total": total,
            "correct": correct,
            "accuracy_pct": round(accuracy, 2),
            "recall_pct": round(recall, 2),
            "precision_pct": round(precision, 2),
            "details": results,
        }, f, indent=2, ensure_ascii=False)
    print(f"  Results:   {RESULTS_JSON}")


if __name__ == "__main__":
    cases = build_dataset()
